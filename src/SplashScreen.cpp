// ═══════════════════════════════════════════════════════════════════════════
//  HELPERS DE DIBUJO
// ═══════════════════════════════════════════════════════════════════════════
static void drawAjoloteRow(int x0, int y0, int row, uint16_t color) {
    int bytesPerRow = AJOLOTE_WIDTH / 8;
    for (int byteIdx = 0; byteIdx < bytesPerRow; byteIdx++) {
        uint8_t bits = pgm_read_byte(
            &AJOLOTE_BMP[row * bytesPerRow + byteIdx]);
        if (bits == 0) continue;
        for (int bit = 0; bit < 8; bit++) {
            if (bits & (0x80 >> bit)) {
                tft.drawPixel(x0 + byteIdx * 8 + bit, y0 + row, color);
            }
        }
    }
}

static void drawAjoloteFull(int x0, int y0, uint16_t color) {
    for (int r = 0; r < AJOLOTE_HEIGHT; r++) {
        drawAjoloteRow(x0, y0, r, color);
    }
}

// ═══════════════════════════════════════════════════════════════════════════
//  ANIMACIÓN SCAN-IN
// ═══════════════════════════════════════════════════════════════════════════
static void animateScanIn(int x0, int y0) {
    const int SCAN_LINE_HEIGHT = 2;
    for (int r = 0; r < AJOLOTE_HEIGHT; r++) {
        drawAjoloteRow(x0, y0, r, UI_MAIN);
        int scanY = y0 + r + SCAN_LINE_HEIGHT;
        if (scanY < y0 + AJOLOTE_HEIGHT) {
            tft.drawFastHLine(x0, scanY, AJOLOTE_WIDTH, UI_SELECT);
        }
        delay(7);
        if (scanY < y0 + AJOLOTE_HEIGHT) {
            tft.drawFastHLine(x0, scanY, AJOLOTE_WIDTH, TFT_BLACK);
        }
    }
}

// ═══════════════════════════════════════════════════════════════════════════
//  ANIMACIÓN TYPE-ON
// ═══════════════════════════════════════════════════════════════════════════
static void typeOn(int x, int y, const String& txt, uint16_t color,
                   int size, FontType font, int msPerChar) {
    for (int i = 0; i < (int)txt.length(); i++) {
        String nextChar = txt.substring(i, i + 1);
        int charX = x + getTextWidth(txt.substring(0, i), size, font);
        if (font == FONT_BIG) drawStringBig(charX, y, nextChar, color, size);
        else drawStringCustom(charX, y, nextChar, color, size);
        delay(msPerChar);
    }
}

static void drawSplashFrame() {
    tft.fillScreen(BOOT_BG);

    tft.drawFastHLine(0, 0, 320, BOOT_LINE);
    tft.drawFastHLine(0, 239, 320, BOOT_LINE);
    tft.drawFastVLine(0, 0, 240, BOOT_LINE);
    tft.drawFastVLine(319, 0, 240, BOOT_LINE);

    tft.drawFastHLine(2, 2, 316, BOOT_LINE);
    tft.drawFastHLine(2, 237, 316, BOOT_LINE);
    tft.drawFastVLine(2, 2, 236, BOOT_LINE);
    tft.drawFastVLine(317, 2, 236, BOOT_LINE);

    tft.drawFastHLine(0, 34, 320, BOOT_LINE);
    tft.drawFastHLine(0, 206, 320, BOOT_LINE);

    // Reinforce the right edge after horizontal separators are drawn.
    tft.drawFastVLine(319, 0, 240, BOOT_LINE);
    tft.drawFastVLine(317, 2, 236, BOOT_LINE);
}

// ═══════════════════════════════════════════════════════════════════════════
//  LOADING STEP
// ═══════════════════════════════════════════════════════════════════════════
static void drawLoadingStep(const String& stepText, int progress,
                            uint16_t toneFreq) {
    int statusY = 175;
    tft.fillRect(10, statusY, 300, 14, BOOT_BG);
    drawStringCustom(10, statusY, stepText, BOOT_MUTED, 1);

    int barX = 10, barY = 190, barW = 300, barH = 8;
    tft.drawRect(barX, barY, barW, barH, BOOT_CYAN);

    int fillW = (barW - 2) * progress / 100;
    tft.fillRect(barX + 1, barY + 1, barW - 2, barH - 2, BOOT_PANEL);
    if (fillW > 0) {
        tft.fillRect(barX + 1, barY + 1, fillW, barH - 2, BOOT_ORANGE);
    }
    beep(toneFreq, 40);
}

// ═══════════════════════════════════════════════════════════════════════════
//  MAIN · SPLASH SCREEN
// ═══════════════════════════════════════════════════════════════════════════
void runSplashScreen() {
    drawSplashFrame();

    drawStringCustom(12, 12, "MINIMAL MODE", BOOT_TEXT, 1);
    drawStringCustom(236, 12, "BUILD 2026", BOOT_TEXT, 1);

    tft.drawRect(34, 56, 252, 94, BOOT_LINE);
    tft.drawFastVLine(50, 64, 78, BOOT_LINE);

    String title = "ESP32 TOOLS";
    int titleX = (320 - getTextWidth(title, 2, FONT_BIG)) / 2;
    typeOn(titleX, 78, title, BOOT_TEXT, 2, FONT_BIG, 35);

    String sub = "BWifiKill";
    int subX = (320 - getTextWidth(sub, 1, FONT_BIG)) / 2;
    typeOn(subX, 120, sub, TFT_RED, 1, FONT_BIG, 25);

    drawLoadingStep("NVS READY",         25, 1200);
    delay(120);
    drawLoadingStep("DISPLAY READY",     50, 1600);
    delay(120);
    drawLoadingStep("DUAL NRF READY",    75, 2000);
    delay(120);
    drawLoadingStep("INTERFACE READY",  100, 2600);
    delay(220);

    unsigned long bootCount = nvsGetULong("boot_cnt", 0);
    String bootInfo = "Boot #" + String(bootCount);
    drawStringCustom(10, 218, bootInfo, BOOT_TEXT, 1);

    String pressText = "PRESS OK TO CONTINUE";
    int pressW = getTextWidth(pressText, 1, FONT_SMALL);
    int pressX = 310 - pressW;
    int pressY = 218;

    beep(2400, 60);
    delay(40);
    beep(3000, 80);

    tft.fillRect(pressX - 4, pressY - 2, pressW + 8, 12, BOOT_BG);
    drawStringCustom(pressX, pressY, pressText, BOOT_TEXT, 1);

    while (digitalRead(BTN_OK) == HIGH) {
        delay(20);
    }

    beep(2200, 50);
    delay(30);
    beep(2800, 50);
    delay(30);
    beep(3200, 80);

    while (digitalRead(BTN_OK) == LOW) delay(5);
    delay(100);
}
